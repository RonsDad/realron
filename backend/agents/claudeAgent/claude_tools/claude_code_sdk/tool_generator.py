"""
Tool Generator using Claude Code SDK
Based on documented query() function patterns from research
"""

import os
import asyncio
import logging
from typing import Dict, Any, Optional, List
from datetime import datetime

# Import Claude Code SDK based on documented patterns
try:
    from claude_code_sdk import query, ClaudeCodeOptions, Message
    CLAUDE_CODE_SDK_AVAILABLE = True
except ImportError:
    CLAUDE_CODE_SDK_AVAILABLE = False
    logging.warning("Claude Code SDK not available")

logger = logging.getLogger(__name__)


class ToolGenerator:
    """Generates healthcare tools using Claude Code SDK query() function"""
    
    def __init__(self):
        self.api_key = os.getenv('ANTHROPIC_API_KEY')
        if not self.api_key:
            raise ValueError("ANTHROPIC_API_KEY environment variable is required")
        
        if not CLAUDE_CODE_SDK_AVAILABLE:
            raise ImportError("Claude Code SDK is not installed. Run: pip install claude-code-sdk")
    
    async def generate_tool(self, intent: str, context: Dict[str, Any]) -> Dict[str, Any]:
        """
        Generate a healthcare tool using Claude Code SDK
        Based on documented query() patterns from research
        """
        try:
            # Build the prompt for tool generation
            prompt = self._build_tool_prompt(intent, context)
            
            # Configure options based on documented patterns
            options = ClaudeCodeOptions(
                allowed_tools=["Write"],  # Only Write tool needed for HTML generation
                permission_mode='bypassPermissions',  # Skip permission checks for speed
                max_turns=10,  # Allow more turns for complex tools
                system_prompt=self._get_system_prompt(),
                cwd=os.getcwd()  # Set working directory
            )
            
            # Track generation progress
            generated_html = ""
            tool_name = context.get('tool_type', 'Healthcare Tool')
            messages_received = []
            
            # Use the documented async for pattern
            async for message in query(prompt=prompt, options=options):
                # Store all messages for debugging
                messages_received.append(message)
                logger.info(f"Received message type: {type(message).__name__}")
                
                # Handle different message types
                if hasattr(message, 'content'):
                    # AssistantMessage has content attribute with list of blocks
                    if hasattr(message.content, '__iter__'):
                        for block in message.content:
                            # Check for TextBlock
                            if hasattr(block, 'text'):
                                text = block.text
                                logger.info(f"Text block content preview: {text[:200]}...")
                                
                                # Look for HTML in the text
                                if '<html' in text.lower() or '<!doctype' in text.lower():
                                    generated_html = self._extract_html_from_text(text)
                                    logger.info(f"Extracted HTML: {len(generated_html)} characters")
                            
                            # Check for ToolUseBlock
                            elif hasattr(block, 'type') and block.type == 'tool_use':
                                logger.info(f"Tool use block: {block.name if hasattr(block, 'name') else 'unknown'}")
                                if hasattr(block, 'input') and isinstance(block.input, dict):
                                    # Check if Write tool was used with HTML content
                                    if block.input.get('content') and '<html' in str(block.input.get('content')).lower():
                                        generated_html = block.input.get('content')
                                        logger.info(f"Found HTML in tool use: {len(generated_html)} characters")
            
            # If no HTML was generated in the response, create from template
            if not generated_html:
                logger.warning("No HTML generated by SDK, using fallback template")
                generated_html = await self._generate_fallback_html(intent, context)
            
            # Return the generated tool
            return {
                "success": True,
                "tool_name": tool_name,
                "html": generated_html,
                "messages_count": len(messages_received),
                "timestamp": datetime.now().isoformat()
            }
            
        except Exception as e:
            logger.error(f"Error generating tool: {str(e)}")
            # Fallback to template-based generation
            html = await self._generate_fallback_html(intent, context)
            return {
                "success": True,
                "tool_name": context.get('tool_type', 'Healthcare Tool'),
                "html": html,
                "error": str(e),
                "fallback": True
            }
    
    def _build_tool_prompt(self, intent: str, context: Dict[str, Any]) -> str:
        """Build the prompt for tool generation"""
        tool_type = context.get('tool_type', 'healthcare tool')
        patient_name = context.get('patient_name', 'Patient')
        
        prompt = f"""Create a complete HTML file for a {tool_type} tool.

Patient Context:
- Name: {patient_name}
- Request: {intent}
- Medications: {context.get('medications', [])}
- Conditions: {context.get('conditions', [])}

Requirements:
1. Generate a COMPLETE HTML file with ALL styles inline
2. Mobile-first responsive design
3. Beautiful blue gradient theme (#667eea to #764ba2)
4. Interactive JavaScript functionality inline
5. NO external dependencies - everything in one file
6. Patient-friendly language - no technical jargon
7. The file should be fully functional when opened in a browser

Create the complete HTML content including <!DOCTYPE html> declaration."""
        
        return prompt
    
    def _get_system_prompt(self) -> str:
        """System prompt for healthcare tool generation"""
        return """You are a healthcare tool generator. You create beautiful, 
        functional, self-contained HTML tools for patients. All tools must be:
        1. Completely self-contained (no external dependencies)
        2. Mobile-friendly and responsive
        3. Accessible and easy to use
        4. Professional with a healthcare theme
        5. Written in patient-friendly language"""
    
    def _extract_html_from_text(self, text: str) -> str:
        """Extract HTML content from text response"""
        # Look for HTML markers
        html_start = text.find('<!DOCTYPE') if '<!DOCTYPE' in text else text.find('<html')
        html_end = text.rfind('</html>') + 7 if '</html>' in text else len(text)
        
        if html_start >= 0 and html_end > html_start:
            return text[html_start:html_end]
        
        # Try code block extraction
        if '```html' in text:
            start = text.find('```html') + 7
            end = text.find('```', start)
            if end > start:
                return text[start:end].strip()
        
        return ""
    
    async def _generate_fallback_html(self, intent: str, context: Dict[str, Any]) -> str:
        """Generate HTML using templates when SDK fails"""
        tool_type = context.get('tool_type', 'Healthcare Tool')
        patient_name = context.get('patient_name', 'Patient')
        
        # Basic responsive template
        return f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{patient_name}'s {tool_type}</title>
    <style>
        * {{
            margin: 0;
            padding: 0;
            box-sizing: border-box;
        }}
        
        body {{
            font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
            background: linear-gradient(135deg, #667eea 0%, #764ba2 100%);
            color: #333;
            min-height: 100vh;
            padding: 20px;
        }}
        
        .container {{
            max-width: 800px;
            margin: 0 auto;
            background: white;
            border-radius: 16px;
            box-shadow: 0 4px 6px rgba(0, 0, 0, 0.1);
            padding: 2rem;
        }}
        
        h1 {{
            color: #667eea;
            margin-bottom: 1.5rem;
            text-align: center;
        }}
        
        .info {{
            background: #f8f9fa;
            padding: 1rem;
            border-radius: 8px;
            margin: 1rem 0;
        }}
        
        .button {{
            background: #667eea;
            color: white;
            border: none;
            padding: 12px 24px;
            border-radius: 8px;
            font-size: 16px;
            cursor: pointer;
            transition: background 0.3s;
            display: block;
            margin: 2rem auto;
        }}
        
        .button:hover {{
            background: #5a67d8;
        }}
    </style>
</head>
<body>
    <div class="container">
        <h1>{patient_name}'s {tool_type}</h1>
        <div class="info">
            <p>This tool was created based on your request: "{intent}"</p>
            <p>It's personalized just for you and ready to use.</p>
        </div>
        <button class="button" onclick="alert('Tool functionality coming soon!')">
            Get Started
        </button>
    </div>
</body>
</html>"""


# Global instance
tool_generator = ToolGenerator() if CLAUDE_CODE_SDK_AVAILABLE else None